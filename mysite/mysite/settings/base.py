def database_config() -> dict:
    database_url = os.getenv("DATABASE_URL")
    if not database_url:
        return {
            "ENGINE": "django.db.backends.sqlite3",
            "NAME": BASE_DIR / "db.sqlite3",
        }

    if database_url.startswith("postgres://") or database_url.startswith("postgresql://"):
        parsed = urlparse(database_url)
        database_name = parsed.path.lstrip("/")
        if not database_name:
            raise ImproperlyConfigured("DATABASE_URL must include a database name")

        query_options = parse_qs(parsed.query)
        options = {
            key: values[-1]
            for key, values in query_options.items()
            if values and key in {"sslmode", "connect_timeout"}
        }

        return {
            "ENGINE": "django.db.backends.postgresql",
            "NAME": database_name,
            "USER": parsed.username or "",
            "PASSWORD": parsed.password or "",
            "HOST": parsed.hostname or "",
            "PORT": str(parsed.port or ""),
            "CONN_MAX_AGE": 600,
            "CONN_HEALTH_CHECKS": True,
            **({"OPTIONS": options} if options else {}),
        }

    raise ImproperlyConfigured("Unsupported DATABASE_URL scheme. Use postgres:// or postgresql://")


DATABASES = {"default": database_config()}


